    private MethodSpec generateExistsById(EntityMetadata entity, String idType) {
        ColumnMapping id = entity.getIdColumn();
        String sql = "SELECT COUNT(*) FROM " + entity.getTableName() + " WHERE " + id.columnName() + " = ?";

        MethodSpec.Builder builder = MethodSpec.methodBuilder("existsById")
                .addAnnotation(Override.class)
                .addModifiers(Modifier.PUBLIC)
                .returns(boolean.class)
                .addParameter(ClassName.bestGuess(idType), "id")
                .beginControlFlow("try (var conn = getConnection(); var stmt = conn.prepareStatement($S))", sql);

        if (id.isUuid()) {
            builder.addStatement("stmt.setString(1, id.toString())");
        } else {
            builder.addStatement("stmt.$L(1, id)", id.jdbcSetter());
        }

        builder.beginControlFlow("try (var rs = stmt.executeQuery())")
                .addStatement("rs.next()")
                .addStatement("return rs.getLong(1) > 0")
                .endControlFlow()
                .nextControlFlow("catch ($T e)", ClassName.get("java.sql", "SQLException"))
                .addStatement("throw new $T(e)", RuntimeException.class)
                .endControlFlow();

        return builder.build();
    }

    private MethodSpec generateQueryMethod(QueryMethodDefinition qm, EntityMetadata entity, ClassName entityType) {
        ClassName listType = ClassName.get("java.util", "List");
        ClassName arrayListType = ClassName.get("java.util", "ArrayList");
        ClassName optionalType = ClassName.get("java.util", "Optional");

        TypeName returnType;
        if (qm.isCount()) returnType = TypeName.LONG;
        else if (qm.isExists()) returnType = TypeName.BOOLEAN;
        else if (qm.returnsList()) returnType = ParameterizedTypeName.get(listType, entityType);
        else if (qm.returnsOptional()) returnType = ParameterizedTypeName.get(optionalType, entityType);
        else returnType = entityType;

        MethodSpec.Builder builder = MethodSpec.methodBuilder(qm.methodName())
                .addAnnotation(Override.class)
                .addModifiers(Modifier.PUBLIC)
                .returns(returnType);

        for (QueryMethodDefinition.QueryParam param : qm.params()) {
            builder.addParameter(ClassName.bestGuess(param.qualifiedParamType()), param.paramName());
        }

        if (qm.params().isEmpty()) {
            builder.beginControlFlow("try (var conn = getConnection(); var stmt = conn.prepareStatement($S); var rs = stmt.executeQuery())", qm.sql());
        } else {
            builder.beginControlFlow("try (var conn = getConnection(); var stmt = conn.prepareStatement($S))", qm.sql());
            for (int i = 0; i < qm.params().size(); i++) {
                QueryMethodDefinition.QueryParam param = qm.params().get(i);
                // Check if param is UUID type
                if ("java.util.UUID".equals(param.qualifiedParamType())) {
                    builder.addStatement("stmt.setString($L, $L.toString())", i + 1, param.paramName());
                } else {
                    ColumnMapping col = findColumnForParam(entity, param);
                    String setter = col != null ? col.jdbcSetter() : "setObject";
                    builder.addStatement("stmt.$L($L, $L)", setter, i + 1, param.paramName());
                }
            }
            builder.beginControlFlow("try (var rs = stmt.executeQuery())");
        }

        if (qm.isCount()) {
            builder.addStatement("rs.next()");
            builder.addStatement("return rs.getLong(1)");
        } else if (qm.isExists()) {
            builder.addStatement("rs.next()");
            builder.addStatement("return rs.getLong(1) > 0");
        } else if (qm.returnsList()) {
            builder.addStatement("$T<$T> results = new $T<>()", listType, entityType, arrayListType);
            builder.beginControlFlow("while (rs.next())");
            builder.addStatement("results.add(mapRow(rs))");
            builder.endControlFlow();
            builder.addStatement("return results");
        } else if (qm.returnsOptional()) {
            builder.beginControlFlow("if (rs.next())");
            builder.addStatement("return $T.of(mapRow(rs))", optionalType);
            builder.endControlFlow();
            builder.addStatement("return $T.empty()", optionalType);
        } else {
            builder.beginControlFlow("if (rs.next())");
            builder.addStatement("return mapRow(rs)");
            builder.endControlFlow();
            builder.addStatement("return null");
        }

        if (!qm.params().isEmpty()) {
            builder.endControlFlow(); // close inner try (rs)
        }
        builder.nextControlFlow("catch ($T e)", ClassName.get("java.sql", "SQLException"));
        builder.addStatement("throw new $T(e)", RuntimeException.class);
        builder.endControlFlow();

        return builder.build();
    }

    // ---- @OneToMany child methods ----

    private void generateChildMethods(TypeSpec.Builder classBuilder, OneToManyRelation rel) {
        EntityMetadata child = rel.childMeta();
        ClassName childType = ClassName.bestGuess(child.getQualifiedName());
        String suffix = capitalize(rel.fieldName()); // "Items", "Addons", "ReconData"

        // mapXxxRow(ResultSet)
        classBuilder.addMethod(generateChildMapRow(child, childType, suffix, rel));

        // loadXxx(Connection, UUID)
        classBuilder.addMethod(generateLoadChildren(child, childType, suffix, rel));

        // saveXxx(Connection, UUID, List<Child>)
        classBuilder.addMethod(generateSaveChildren(child, childType, suffix, rel));

        // deleteXxx(Connection, UUID)
        classBuilder.addMethod(generateDeleteChildren(child, childType, suffix, rel));

        // Recurse for nested relations
        for (OneToManyRelation nested : rel.nested()) {
            generateChildMethods(classBuilder, nested);
        }
    }

    private MethodSpec generateChildMapRow(EntityMetadata child, ClassName childType,
                                            String suffix, OneToManyRelation rel) {
        MethodSpec.Builder builder = MethodSpec.methodBuilder("map" + suffix + "Row")
                .addModifiers(Modifier.PRIVATE)
                .returns(childType)
                .addParameter(ClassName.get("java.sql", "ResultSet"), "rs")
                .addException(ClassName.get("java.sql", "SQLException"))
                .addStatement("var child = new $T()", childType);

        for (ColumnMapping col : child.getRegularColumns()) {
            String setter = setterName(col);
            if (col.isElementCollection()) {
                builder.beginControlFlow("try");
                builder.addStatement("String $LJson = rs.getString($S)", col.fieldName(), col.columnName());
                builder.beginControlFlow("if ($LJson != null)", col.fieldName());
                builder.addStatement("child.$L(objectMapper.readValue($LJson, objectMapper.getTypeFactory().constructCollectionType(java.util.List.class, $L.class)))",
                        setter, col.fieldName(), col.elementCollectionItemType());
                builder.endControlFlow();
                builder.nextControlFlow("catch (Exception e)");
                builder.endControlFlow();
            } else if (col.isUuid()) {
                builder.addStatement("String $LStr = rs.getString($S)", col.fieldName(), col.columnName());
                builder.beginControlFlow("if ($LStr != null)", col.fieldName());
                builder.addStatement("child.$L(java.util.UUID.fromString($LStr))", setter, col.fieldName());
                builder.endControlFlow();
            } else if (col.needsTypecastOnGet()) {
                builder.addStatement("child.$L(rs.$L($S, $T.class))",
                        setter, col.jdbcGetter(), col.columnName(),
                        ClassName.bestGuess(col.qualifiedJavaType()));
            } else {
                builder.addStatement("child.$L(rs.$L($S))",
                        setter, col.jdbcGetter(), col.columnName());
            }
        }

        builder.addStatement("return child");
        return builder.build();
    }

    private MethodSpec generateLoadChildren(EntityMetadata child, ClassName childType,
                                             String suffix, OneToManyRelation rel) {
        String sql = "SELECT * FROM " + child.getTableName() + " WHERE " + rel.fkColumnName() + " = ?";
        ClassName listType = ClassName.get("java.util", "List");
        ClassName arrayListType = ClassName.get("java.util", "ArrayList");

        MethodSpec.Builder builder = MethodSpec.methodBuilder("load" + suffix)
                .addModifiers(Modifier.PRIVATE)
                .returns(ParameterizedTypeName.get(listType, childType))
                .addParameter(ClassName.get("java.sql", "Connection"), "conn")
                .addParameter(ClassName.get("java.util", "UUID"), "parentId")
                .addException(ClassName.get("java.sql", "SQLException"));

        builder.beginControlFlow("try (var stmt = conn.prepareStatement($S))", sql)
                .addStatement("stmt.setString(1, parentId.toString())")
                .beginControlFlow("try (var rs = stmt.executeQuery())")
                .addStatement("$T<$T> results = new $T<>()", listType, childType, arrayListType)
                .beginControlFlow("while (rs.next())")
                .addStatement("var child = map$LRow(rs)", suffix);

        // Load nested children
        for (OneToManyRelation nested : rel.nested()) {
            String nestedSuffix = capitalize(nested.fieldName());
            builder.addStatement("child.$L(load$L(conn, child.getId()))",
                    nested.setterName(), nestedSuffix);
        }

        builder.addStatement("results.add(child)")
                .endControlFlow()
                .addStatement("return results")
                .endControlFlow()
                .endControlFlow();

        return builder.build();
    }

    private MethodSpec generateSaveChildren(EntityMetadata child, ClassName childType,
                                             String suffix, OneToManyRelation rel) {
        ClassName listType = ClassName.get("java.util", "List");

        // Build INSERT SQL for child
        List<ColumnMapping> insertCols = child.getRegularColumns().stream()
                .filter(c -> !c.isCreatedAt() && !c.isUpdatedAt())
                .toList();

        StringBuilder colNames = new StringBuilder();
        StringBuilder placeholders = new StringBuilder();
        for (int i = 0; i < insertCols.size(); i++) {
            if (i > 0) { colNames.append(", "); placeholders.append(", "); }
            colNames.append(insertCols.get(i).columnName());
            placeholders.append("?");
        }
        // Add audit columns
        boolean hasCreatedAt = child.getRegularColumns().stream().anyMatch(ColumnMapping::isCreatedAt);
        boolean hasUpdatedAt = child.getRegularColumns().stream().anyMatch(ColumnMapping::isUpdatedAt);
        if (hasCreatedAt) { colNames.append(", created_at"); placeholders.append(", ?"); }
        if (hasUpdatedAt) { colNames.append(", updated_at"); placeholders.append(", ?"); }

        String insertSql = "INSERT INTO " + child.getTableName() +
                " (" + colNames + ") VALUES (" + placeholders + ")";

        MethodSpec.Builder builder = MethodSpec.methodBuilder("save" + suffix)
                .addModifiers(Modifier.PRIVATE)
                .addParameter(ClassName.get("java.sql", "Connection"), "conn")
                .addParameter(ClassName.get("java.util", "UUID"), "parentId")
                .addParameter(ParameterizedTypeName.get(listType, childType), "children")
                .addException(ClassName.get("java.sql", "SQLException"));

        builder.beginControlFlow("if (children == null)")
                .addStatement("return")
                .endControlFlow();
        builder.addStatement("var now = java.time.LocalDateTime.now()");

        builder.beginControlFlow("for (var child : children)");

        // Set FK
        String fkSetter = "set" + capitalize(rel.fkFieldName());
        builder.addStatement("child.$L(parentId)", fkSetter);

        // Generate UUID if null
        if (child.hasUuidId()) {
            String idGetter = getterName(child.getIdColumn());
            String idSetter = setterName(child.getIdColumn());
            builder.beginControlFlow("if (child.$L() == null)", idGetter);
            builder.addStatement("child.$L(java.util.UUID.randomUUID())", idSetter);
            builder.endControlFlow();
        }

        builder.beginControlFlow("try (var stmt = conn.prepareStatement($S))", insertSql);

        int idx = 1;
        for (ColumnMapping col : insertCols) {
            builder.addStatement(setParamStatement(col, idx++, "child"));
        }
        if (hasCreatedAt) builder.addStatement("stmt.setObject($L, now)", idx++);
        if (hasUpdatedAt) builder.addStatement("stmt.setObject($L, now)", idx++);

        builder.addStatement("stmt.executeUpdate()");
        builder.endControlFlow(); // close stmt

        // Recursively save nested children
        for (OneToManyRelation nested : rel.nested()) {
            String nestedSuffix = capitalize(nested.fieldName());
            builder.addStatement("save$L(conn, child.getId(), child.$L())",
                    nestedSuffix, nested.getterName());
        }

        builder.endControlFlow(); // close for loop

        return builder.build();
    }

    private MethodSpec generateDeleteChildren(EntityMetadata child, ClassName childType,
                                               String suffix, OneToManyRelation rel) {
        String selectIdsSql = "SELECT id FROM " + child.getTableName() + " WHERE " + rel.fkColumnName() + " = ?";
        String deleteSql = "DELETE FROM " + child.getTableName() + " WHERE " + rel.fkColumnName() + " = ?";

        MethodSpec.Builder builder = MethodSpec.methodBuilder("delete" + suffix)
                .addModifiers(Modifier.PRIVATE)
                .addParameter(ClassName.get("java.sql", "Connection"), "conn")
                .addParameter(ClassName.get("java.util", "UUID"), "parentId")
                .addException(ClassName.get("java.sql", "SQLException"));

        // If there are nested relations, delete grandchildren first
        if (!rel.nested().isEmpty()) {
            builder.beginControlFlow("try (var stmt = conn.prepareStatement($S))", selectIdsSql)
                    .addStatement("stmt.setString(1, parentId.toString())")
                    .beginControlFlow("try (var rs = stmt.executeQuery())")
                    .beginControlFlow("while (rs.next())")
                    .addStatement("var childId = java.util.UUID.fromString(rs.getString(1))");

            for (OneToManyRelation nested : rel.nested()) {
                String nestedSuffix = capitalize(nested.fieldName());
                builder.addStatement("delete$L(conn, childId)", nestedSuffix);
            }

            builder.endControlFlow() // while
                    .endControlFlow() // rs
                    .endControlFlow(); // stmt
        }

        // Delete children
        builder.beginControlFlow("try (var stmt = conn.prepareStatement($S))", deleteSql)
                .addStatement("stmt.setString(1, parentId.toString())")
                .addStatement("stmt.executeUpdate()")
                .endControlFlow();

        return builder.build();
    }

