    private ColumnMapping findColumnForParam(EntityMetadata entity, QueryMethodDefinition.QueryParam param) {
        for (ColumnMapping col : entity.getColumns()) {
            if (col.fieldName().equalsIgnoreCase(param.paramName())) return col;
        }
        return null;
    }

    private String getterName(ColumnMapping col) {
        String type = col.qualifiedJavaType();
        String field = col.fieldName();
        boolean isBooleanType = "boolean".equals(type) || "java.lang.Boolean".equals(type);

        if (isBooleanType) {
            // If field already starts with "is", use it directly as getter
            if (field.startsWith("is") && field.length() > 2 && Character.isUpperCase(field.charAt(2))) {
                return field; // isActive -> isActive(), isMainMenu -> isMainMenu()
            }
            return "is" + capitalize(field); // active -> isActive()
        }
        return "get" + capitalize(field);
    }

    private String setterName(ColumnMapping col) {
        String type = col.qualifiedJavaType();
        String field = col.fieldName();
        boolean isBooleanType = "boolean".equals(type) || "java.lang.Boolean".equals(type);

        if (isBooleanType && field.startsWith("is") && field.length() > 2 && Character.isUpperCase(field.charAt(2))) {
            // isMainMenu -> setMainMenu (strip "is" prefix)
            return "set" + field.substring(2);
        }
        return "set" + capitalize(field);
    }

    private String capitalize(String name) {
        if (name == null || name.isEmpty()) return name;
        return Character.toUpperCase(name.charAt(0)) + name.substring(1);
    }
